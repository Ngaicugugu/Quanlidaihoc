    private void init() {
        this.setLocationRelativeTo(null);
        accessRight();
        fillcbokhoa();
        this.row = -1;
    }

    private void filltotable() {
        DefaultTableModel model = (DefaultTableModel) tblsinhvien.getModel();
        model.setRowCount(0);
        Lop lop = (Lop) cbolop.getSelectedItem();
        if (lop != null) {
            List<SinhVien> list = dao.selectByLop(lop.getMaLop());
            for (SinhVien sv : list) {
                Object[] row = {
                    sv.getMaSV(),
                    sv.getHoTen(),
                    sv.getNgaySinh(),
                    sv.isGioitinh() ? "Nam" : "Nữ",
                    sv.getDiaChi(),
                    sv.getMaLop()
                };
                model.addRow(row);
            }
        }
    }

    void edit() {
        String masv = (String) tblsinhvien.getValueAt(this.row, 0);
        SinhVien sv = dao.selectById(masv);
        this.setForm(sv);
    }

    void setForm(SinhVien sv) {
        txtmasv.setText(sv.getMaSV());
        txthoten.setText(sv.getHoTen());
        txtngaysinh.setText(sv.getNgaySinh());
        rdonam.setSelected(sv.isGioitinh());
        rdonu.setSelected(!sv.isGioitinh());
        txtdiachi.setText(sv.getDiaChi());
        txtmalop.setText(sv.getMaLop());
    }

    void clearForm() {
        SinhVien sv = new SinhVien();
        this.setForm(sv);
        this.row = -1;
    }

    SinhVien getForm() {
        SinhVien sv = new SinhVien();
        sv.setMaSV(txtmasv.getText());
        sv.setHoTen(txthoten.getText());
        sv.setNgaySinh(txtngaysinh.getText());
        sv.setGioitinh(rdonam.isSelected());
        sv.setDiaChi(txtdiachi.getText());
        sv.setMaLop(txtmalop.getText());
        return sv;
    }

    void insert() {
        StringBuilder sb = new StringBuilder();
        DataValidation.validateEmp(txtmasv, sb, "Không được bỏ trống mã sinh viên");
        DataValidation.validateEmp(txthoten, sb, "Không được bỏ trống họ tên");
        DataValidation.validateEmp(txtdiachi, sb, "Không được bỏ trống địa chỉ");
        DataValidation.validateEmp(txtngaysinh, sb, "Không được bỏ trống ngày sinh");
        DataValidation.validateRole(buttonGroup1, sb, "Phải chọn giới tính");
        DataValidation.validateEmp(txtmalop, sb, "Không được để trống mã lớp");
        List<SinhVien> list = dao.selectAll();
        for (SinhVien sv : list) {
            if (sv.getMaSV().equalsIgnoreCase(txtmasv.getText())) {
                MsgBox.showErrorDialog(null, "Không được trùng mã sinh viên");
                return;
            }
        }
        
        if (sb.length() > 0) {
            MsgBox.showErrorDialog(null, sb.toString());
            return;
        }

        SinhVien sv = getForm();
        try {
            dao.insert(sv);
            this.filltotable();
            this.clearForm();
            MsgBox.alert(this, "Thêm mới thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Thêm mới thất bại!");
        }
    }

