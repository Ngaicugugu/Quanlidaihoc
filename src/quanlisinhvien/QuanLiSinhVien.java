    void delete() {
        StringBuilder sb = new StringBuilder();
        DataValidation.validateEmp(txtmasv, sb, "Không mã sinh viên để xóa");
        
        if (sb.length() > 0) {
            MsgBox.showErrorDialog(null, sb.toString());
            return;
        }
        
        String masv = txtmasv.getText();
        try {
            dao.delete(masv);
            this.filltotable();
            this.clearForm();
            MsgBox.alert(this, "Xóa thành công!");
        } catch (Exception e) {
            MsgBox.alert(this, "Xóa thất bại!");
        }
    }

    void fillcbolop() {
        try {
            DefaultComboBoxModel model = (DefaultComboBoxModel) cbolop.getModel();
            model.removeAllElements();
            Khoa khoa = (Khoa) cbokhoa.getSelectedItem();
            if (khoa != null) {
                List<Lop> list = ldao.selectByKhoa(khoa.getMaKhoa());
                for (Lop lop : list) {
                    model.addElement(lop);
                }
                this.filltotable();
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }

    private void fillcbokhoa() {
        DefaultComboBoxModel model = (DefaultComboBoxModel) cbokhoa.getModel();
        model.removeAllElements();
        List<Khoa> list = kdao.selectAll();
        for (Khoa khoa : list) {
            model.addElement(khoa);
        }
        this.fillcbolop();
    }
